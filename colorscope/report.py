    def generate_text(self, results: List[ContrastResult],
                      colorblind_results: Optional[Dict] = None,
                      suggestions: Optional[Dict] = None) -> str:
        """
        Generate plain text format report.
        生成纯文本格式报告
        
        Args:
            results: List of contrast check results
            colorblind_results: Optional color blindness simulation results
            suggestions: Optional color suggestions
        
        Returns:
            Plain text string
        """
        lines = []
        
        lines.append("=" * 60)
        lines.append("ColorScope Contrast Report")
        lines.append("=" * 60)
        lines.append(f"Generated: {self.timestamp}")
        lines.append("")
        
        # Summary
        total = len(results)
        passed_aa = sum(1 for r in results if r.aa_normal)
        passed_aaa = sum(1 for r in results if r.aaa_normal)
        
        lines.append("SUMMARY")
        lines.append("-" * 40)
        lines.append(f"Total Checks:     {total}")
        lines.append(f"AA Compliant:     {passed_aa} ({passed_aa/total*100:.1f}%)" if total > 0 else "AA Compliant:     0")
        lines.append(f"AAA Compliant:    {passed_aaa} ({passed_aaa/total*100:.1f}%)" if total > 0 else "AAA Compliant:    0")
        lines.append("")
        
        # Results
        lines.append("CONTRAST RESULTS")
        lines.append("-" * 40)
        
        for result in results:
            aa_status = "PASS" if result.aa_normal else "FAIL"
            aaa_status = "PASS" if result.aaa_normal else "FAIL"
            
            lines.append(f"FG: {result.foreground.to_hex()}  BG: {result.background.to_hex()}")
            lines.append(f"  Ratio: {result.ratio:.2f}:1  AA: {aa_status}  AAA: {aaa_status}")
            lines.append(f"  Level: {result.wcag_level.value}")
            lines.append("")
        
        lines.append("=" * 60)
        
        return "\n".join(lines)
    
    def save_report(self, content: str, filepath: str, format: str = "json"):
        """
        Save report to file.
        保存报告到文件
        
        Args:
            content: Report content
            filepath: Output file path
            format: Output format (json, md, html, txt)
        """
        path = Path(filepath)
        path.parent.mkdir(parents=True, exist_ok=True)
        
        with open(path, "w", encoding="utf-8") as f:
            f.write(content)
        
        return str(path.absolute())
