#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Report generation module for contrast analysis results.
对比度分析结果报告生成模块

Supports multiple output formats: JSON, Markdown, HTML, and plain text.
支持多种输出格式：JSON、Markdown、HTML和纯文本
"""

import json
from datetime import datetime
from typing import List, Optional, Dict, Any
from pathlib import Path

from .contrast import ContrastResult, Color, WCAGLevel
from .colorblind import SimulationResult, ColorBlindnessType


class ReportGenerator:
    """
    Multi-format report generator.
    多格式报告生成器
    
    Generates reports in JSON, Markdown, HTML, and plain text formats.
    """
    
    def __init__(self, language: str = "zh"):
        """
        Initialize the report generator.
        
        Args:
            language: Language for report content ("zh" or "en")
        """
        self.language = language
        self.timestamp = datetime.now().isoformat()
    
    def generate_json(self, results: List[ContrastResult], 
                      colorblind_results: Optional[Dict] = None,
                      suggestions: Optional[Dict] = None) -> str:
        """
        Generate JSON format report.
        生成JSON格式报告
        
        Args:
            results: List of contrast check results
            colorblind_results: Optional color blindness simulation results
            suggestions: Optional color suggestions
        
        Returns:
            JSON string
        """
        report = {
            "metadata": {
                "generated_at": self.timestamp,
                "tool": "ColorScope-CLI",
                "version": "1.0.0",
            },
            "contrast_results": [r.to_dict() for r in results],
        }
        
        if colorblind_results:
            report["colorblind_simulation"] = {
                k: [sr.to_dict() for sr in v] if isinstance(v, list) else v.to_dict()
                for k, v in colorblind_results.items()
            }
        
        if suggestions:
            report["suggestions"] = suggestions
        
        return json.dumps(report, indent=2, ensure_ascii=False)
    
    def generate_markdown(self, results: List[ContrastResult],
                          colorblind_results: Optional[Dict] = None,
                          suggestions: Optional[Dict] = None,
                          title: str = "ColorScope Contrast Report") -> str:
        """
        Generate Markdown format report.
        生成Markdown格式报告
        
        Args:
            results: List of contrast check results
            colorblind_results: Optional color blindness simulation results
            suggestions: Optional color suggestions
            title: Report title
        
        Returns:
            Markdown string
        """
        lines = []
        
        # Header
        lines.append(f"# {title}")
        lines.append("")
        lines.append(f"> Generated by ColorScope-CLI at {self.timestamp}")
        lines.append("")
        
        # Summary
        lines.append("## 📊 Summary")
        lines.append("")
        total = len(results)
        passed_aa = sum(1 for r in results if r.aa_normal)
        passed_aaa = sum(1 for r in results if r.aaa_normal)
        
        lines.append(f"| Metric | Count | Percentage |")
        lines.append(f"|--------|-------|------------|")
        lines.append(f"| Total Checks | {total} | 100% |")
        lines.append(f"| AA Compliant | {passed_aa} | {passed_aa/total*100:.1f}%" if total > 0 else "| AA Compliant | 0 | 0% |")
        lines.append(f"| AAA Compliant | {passed_aaa} | {passed_aaa/total*100:.1f}%" if total > 0 else "| AAA Compliant | 0 | 0% |")
        lines.append("")
        
        # Detailed Results
        lines.append("## 🎨 Contrast Results")
        lines.append("")
        lines.append("| Foreground | Background | Ratio | AA | AAA | Level |")
        lines.append("|------------|------------|-------|-----|------|-------|")
        
        for result in results:
            aa_status = "✅" if result.aa_normal else "❌"
            aaa_status = "✅" if result.aaa_normal else "❌"
            lines.append(
                f"| {result.foreground.to_hex()} | {result.background.to_hex()} | "
                f"{result.ratio:.2f}:1 | {aa_status} | {aaa_status} | {result.wcag_level.value} |"
            )
        lines.append("")
        
        # Color Blindness Simulation
        if colorblind_results:
            lines.append("## 👁️ Color Blindness Simulation")
            lines.append("")
            lines.append("Simulated appearance for users with different types of color vision deficiency:")
            lines.append("")
            
            for blindness_type, sim_results in colorblind_results.items():
                lines.append(f"### {blindness_type.title()}")
                lines.append("")
                if isinstance(sim_results, list):
                    for sr in sim_results:
                        lines.append(f"- Original: {sr.original.to_hex()} → Simulated: {sr.simulated.to_hex()}")
                else:
                    lines.append(f"- {sim_results.description}")
                lines.append("")
        
        # Suggestions
        if suggestions:
            lines.append("## 💡 Suggestions")
            lines.append("")
            if "foreground_suggestions" in suggestions:
                lines.append("### Foreground Adjustments")
                lines.append("")
                for sug in suggestions["foreground_suggestions"][:3]:
                    lines.append(f"- {sug['original']} → {sug['suggested']} (Ratio: {sug['new_ratio']:.2f}:1)")
                lines.append("")
            
            if "background_suggestions" in suggestions:
                lines.append("### Background Adjustments")
                lines.append("")
                for sug in suggestions["background_suggestions"][:3]:
                    lines.append(f"- {sug['original']} → {sug['suggested']} (Ratio: {sug['new_ratio']:.2f}:1)")
                lines.append("")
        
        # Footer
        lines.append("---")
        lines.append("")
        lines.append("*Generated by [ColorScope-CLI](https://github.com/yourusername/colorscope-cli)*")
        
        return "\n".join(lines)
    
